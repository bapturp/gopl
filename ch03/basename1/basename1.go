package main

import "fmt"

func main() {
	fmt.Println(basename("/root/hello.sh"))
	fmt.Println(basename("some.file.txt"))
	fmt.Println(basename("../..hello/world/foo.bar"))
}

// basename removes directory componets and a .suffix.
// e.g., a => a, a.go => a, a/b/c.go => c, a/b.c.go => b.c
func basename(s string) string {
	// Discard last '/' and everything before.
	for i := len(s) - 1; i >= 0; i-- {
		if s[i] == '/' {
			s = s[i+1:]
			break
		}
	}

	// Preserve everything before last '.'.
	for i := len(s) - 1; i >= 0; i-- {
		if s[i] == '.' {
			s = s[:i]
			break
		}
	}

	return s
}
